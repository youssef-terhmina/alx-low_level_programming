/**
 * add - addition
 * @a: 1st nb
 * @b: 2nd nb
 * Return: result
 */

int add(int a, int b)
{
      return (a + b);
}

/**
 * sub - substraction
 * @a: 1st nb
 * @b: 2nd nb
 * Return: result
 */

int sub(int a, int b)
{
      return (a - b);
}

/**
 * mul - multiplication
 * @a: 1st nb
 * @b: 2nd nb
 * Return: result
 */

int mul(int a, int b)
{
      return (a * b);
}

/**
 * div - division
 * @a: 1st nb
 * @b: 2nd nb
 * Return: result
 */

int div(int a, int b)
{
      return (a / b);
}

/**
 * mod - modulus
 * @a: 1st nb
 * @b: 2nd nb
 * Return: result
 */

int mod(int a, int b)
{
      return (a % b);
}
